public class Literals {
    public static void main(String[] args) {
        int a = 100;          // decimal
        int b = 0x1A3F;       // hexadecimal (prefix 0x or 0X)
        long c = 100000L;     // long literal (with L or l)
        double pi = 3.14159;  // default type for decimal
        float rate = 5.5f;      // float literal must end with f or F
        char ch1 = 'A';
        char ch2 = '\n';
        String greeting = "Hello, Java!";
        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println( a);
        System.out.println(pi);
        System.out.println(ch1);
    }
}
